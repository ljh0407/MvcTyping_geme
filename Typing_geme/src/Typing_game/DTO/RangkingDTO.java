package Typing_game.DTO;

public class RangkingDTO {
	
	//필드
	int mno;
	int ss;
	// 생성자
	
	public RangkingDTO(int mno, int ss) {
		this.mno = mno;
		this.ss = ss;
	}

	// 메소드

	public int getMno() {
		return mno;
	}


	public void setMno(int mno) {
		this.mno = mno;
	}

	public int getSs() {
		return ss;
	}


	public void setSs(int ss) {
		this.ss = ss;
	}
	

	
	
}
